char	*ft_strupcase(char *str)
{
	int	idx;

	idx = 0;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str -= 32;
			str++;
		}	
		else
			str++;
		idx++;
	}
	return (str - idx);
}
